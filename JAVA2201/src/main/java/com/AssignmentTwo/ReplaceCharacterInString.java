package com.AssignmentTwo;

public class ReplaceCharacterInString {

    public static void main(String[] args) {

        String n = "Rafi";

        String n2 = n.replace("i","e"); //replacing i with e;

        System.out.println(n2);

        n2 = n2.replace("e","i");

        System.out.println(n2); //replacing e for i;
    }

}
